function initCursor() {
  const cursor = document.querySelector(".cursor"), follower = document.querySelector(".cursor-follower");
  if (!cursor || !follower) return;
  let mouseX = 0, mouseY = 0, cursorX = 0, cursorY = 0, followerX = 0, followerY = 0;
  document.addEventListener("mousemove", (e) => { mouseX = e.clientX; mouseY = e.clientY; });
  document.querySelectorAll("a, button, .portfolio-card, .tag, .filter-btn, .contact-link, .video-modal-close, .nav-toggle").forEach((el) => {
    el.addEventListener("mouseenter", () => { cursor.classList.add("hover"); follower.classList.add("hover"); });
    el.addEventListener("mouseleave", () => { cursor.classList.remove("hover"); follower.classList.remove("hover"); });
  });
  (function animate() {
    cursorX += (mouseX - cursorX) * 0.35; cursorY += (mouseY - cursorY) * 0.35;
    followerX += (mouseX - followerX) * 0.12; followerY += (mouseY - followerY) * 0.12;
    cursor.style.left = cursorX + "px"; cursor.style.top = cursorY + "px";
    follower.style.left = followerX + "px"; follower.style.top = followerY + "px";
    requestAnimationFrame(animate);
  })();
}

function initCanvas() {
  const canvas = document.getElementById("bg-canvas");
  if (!canvas) return;
  const ctx = canvas.getContext("2d");
  let particles = [];
  (function resize() { canvas.width = window.innerWidth; canvas.height = window.innerHeight; })();
  window.addEventListener("resize", resize);
  const count = Math.min(60, Math.floor(window.innerWidth / 25));
  for (let i = 0; i < count; i++) particles.push({ x: Math.random() * canvas.width, y: Math.random() * canvas.height, vx: (Math.random() - 0.5) * 0.35, vy: (Math.random() - 0.5) * 0.35, r: Math.random() * 2 + 0.5, alpha: Math.random() * 0.35 + 0.08 });
  let mpX = 0, mpY = 0;
  document.addEventListener("mousemove", (e) => { mpX = e.clientX; mpY = e.clientY; });
  (function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    for (let p of particles) {
      const dx = mpX - p.x, dy = mpY - p.y, dist = Math.sqrt(dx * dx + dy * dy);
      if (dist < 150) { p.x += dx * 0.001; p.y += dy * 0.001; }
      p.x += p.vx; p.y += p.vy;
      if (p.x < 0 || p.x > canvas.width) p.vx *= -1;
      if (p.y < 0 || p.y > canvas.height) p.vy *= -1;
      ctx.beginPath(); ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = "rgba(109, 179, 216, " + p.alpha + ")"; ctx.fill();
      for (let j = particles.indexOf(p) + 1; j < particles.length; j++) {
        const q = particles[j], dx2 = p.x - q.x, dy2 = p.y - q.y, dist2 = Math.sqrt(dx2 * dx2 + dy2 * dy2);
        if (dist2 < 120) { ctx.beginPath(); ctx.moveTo(p.x, p.y); ctx.lineTo(q.x, q.y); ctx.strokeStyle = "rgba(109, 179, 216, " + (0.04 * (1 - dist2 / 120)) + ")"; ctx.lineWidth = 0.5; ctx.stroke(); }
      }
    }
    requestAnimationFrame(draw);
  })();
}

function initNav() {
  const nav = document.getElementById("nav"), toggle = document.querySelector(".nav-toggle"), links = document.querySelector(".nav-links"), navLinks = document.querySelectorAll(".nav-links a"), sections = document.querySelectorAll("section[id]");
  window.addEventListener("scroll", () => {
    nav.classList.toggle("scrolled", window.scrollY > 60);
    let current = "";
    sections.forEach((s) => { if (window.scrollY >= s.offsetTop - 120) current = s.getAttribute("id"); });
    navLinks.forEach((l) => { l.classList.toggle("active", l.getAttribute("data-nav") === current); });
  });
  if (toggle && links) {
    toggle.addEventListener("click", () => { links.classList.toggle("open"); toggle.classList.toggle("open"); });
    navLinks.forEach((l) => { l.addEventListener("click", () => { links.classList.remove("open"); toggle.classList.remove("open"); }); });
  }
}

